namespace NoireLib.Draw3D.Interaction;

/// <summary>Which mouse button an interaction event came from.</summary>
public enum MouseButton
{
    /// <summary>Left mouse button (the click and drag button).</summary>
    Left = 0,

    /// <summary>Right mouse button (right-click / context).</summary>
    Right = 1,

    /// <summary>Middle mouse button (wheel click).</summary>
    Middle = 2,
}
