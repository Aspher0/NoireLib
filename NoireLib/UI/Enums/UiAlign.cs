namespace NoireLib.UI;

/// <summary>
/// How an element lines up along the edge it was placed against.
/// </summary>
public enum UiAlign
{
    /// <summary>Flush with the start of the edge: the top of a vertical side, the left of a horizontal one.</summary>
    Start,

    /// <summary>Centred on the edge.</summary>
    Center,

    /// <summary>Flush with the end of the edge: the bottom of a vertical side, the right of a horizontal one.</summary>
    End,
}
