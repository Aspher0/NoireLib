namespace NoireLib.UI;

/// <summary>
/// The nine screen anchor points usable by <see cref="UiPosition"/>.
/// </summary>
public enum UiAnchor
{
    /// <summary>
    /// The top left corner of the screen.
    /// </summary>
    TopLeft,

    /// <summary>
    /// The middle of the top edge of the screen.
    /// </summary>
    TopCenter,

    /// <summary>
    /// The top right corner of the screen.
    /// </summary>
    TopRight,

    /// <summary>
    /// The middle of the left edge of the screen.
    /// </summary>
    MiddleLeft,

    /// <summary>
    /// The center of the screen.
    /// </summary>
    MiddleCenter,

    /// <summary>
    /// The middle of the right edge of the screen.
    /// </summary>
    MiddleRight,

    /// <summary>
    /// The bottom left corner of the screen.
    /// </summary>
    BottomLeft,

    /// <summary>
    /// The middle of the bottom edge of the screen.
    /// </summary>
    BottomCenter,

    /// <summary>
    /// The bottom right corner of the screen.
    /// </summary>
    BottomRight,
}
