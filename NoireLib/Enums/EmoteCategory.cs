namespace NoireLib.Enums;

/// <summary>
/// Enumeration representing emote categories.
/// </summary>
public enum EmoteCategory
{
    /// <summary>
    /// Represents general emotes, typically non-looped emotes.
    /// </summary>
    General = 1,

    /// <summary>
    /// Represents special emotes, typically looped emotes.
    /// </summary>
    Special = 2,

    /// <summary>
    /// Represents facial expression emotes.
    /// </summary>
    Expressions = 3,

    /// <summary>
    /// Invalid or unknown emote category.
    /// </summary>
    Unknown = 0,
}
